namespace Mach.Xna.Kinect.Gestures
{
    /// <summary>
    /// List of the recognizable types of the gesture.
    /// </summary>
    public enum GestureType
    {
        /// <summary>
        /// Left hand moved up.
        /// </summary>
        LeftHandUp,
        /// <summary>
        /// Left hand moved to left.
        /// </summary>
        LeftHandLeft,
        /// <summary>
        /// Left hand moved to right.
        /// </summary>
        LeftHandRight,
        /// <summary>
        /// Left hand moved down.
        /// </summary>
        LeftHandDown,
        /// <summary>
        /// Right hand moved up.
        /// </summary>
        RightHandUp,
        /// <summary>
        /// Right hand moved to left.
        /// </summary>
        RightHandLeft,
        /// <summary>
        /// Right hand moved to right.
        /// </summary>
        RightHandRight,
        /// <summary>
        /// Right hand moved down.
        /// </summary>
        RightHandDown,
        /// <summary>
        /// Left hand waved.
        /// </summary>
        LeftHandWave,
        /// <summary>
        /// Right hand waved.
        /// </summary>
        RightHandWave,
    }
}
